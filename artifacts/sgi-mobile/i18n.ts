// ─── Language helpers ──────────────────────────────────────────────────────────
const LANG_KEY = "sgi-lang";
const SUPPORTED = ["it", "en", "es"] as const;
type Lang = typeof SUPPORTED[number];

function getSystemLanguage(): Lang {
  try {
    const locale = Intl.DateTimeFormat().resolvedOptions().locale;
    const code = locale.split("-")[0].toLowerCase() as Lang;
    return SUPPORTED.includes(code) ? code : "it";
  } catch {
    return "it";
  }
}

// ─── Init (synchronous) ───────────────────────────────────────────────────────
i18n.use(initReactI18next).init({
  resources: {
    it: { translation: IT },
    en: { translation: EN },
    es: { translation: ES },
  },
  lng: getSystemLanguage(),
  fallbackLng: "it",
  interpolation: { escapeValue: false },
});

// ─── Async helpers (called from RootLayout) ────────────────────────────────────
export async function initLanguage(): Promise<void> {
  try {
    const saved = await SecureStore.getItemAsync(LANG_KEY);
    if (saved && SUPPORTED.includes(saved as Lang) && saved !== i18n.language) {
      await i18n.changeLanguage(saved);
    }
  } catch {}
}

export async function setLanguage(code: Lang): Promise<void> {
  await i18n.changeLanguage(code);
  try {
    await SecureStore.setItemAsync(LANG_KEY, code);
  } catch {}
}

